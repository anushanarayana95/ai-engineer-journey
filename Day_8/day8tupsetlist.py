# Tuple (cannot change)
employee = ("Ravi", "Chennai", 30000)
print("Tuple data:", employee)

# Set (stores unique values)
cities = {"Chennai", "Mumbai", "Delhi", "Chennai"}
print("Unique cities:", cities)

# Convert list to set to remove duplicates
city_list = ["Chennai", "Mumbai", "Delhi", "Chennai"]
unique_cities = set(city_list)
print("After removing duplicates:", unique_cities)